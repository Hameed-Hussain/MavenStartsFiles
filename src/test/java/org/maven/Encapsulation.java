package org.maven;

public class Encapsulation {

	private int a;
	private String b;
	private long c;
	public int getA() {
		return a;
	}
	public String getB() {
		return b;
	}
	public long getC() {
		return c;
	}
	public void setA(int a) {
		this.a = a;
	}
	public void setB(String b) {
		this.b = b;
	}
	public void setC(long c) {
		this.c = c;
	}
	
}
